            co(1+11,5)>=0 && co(1+11,6)>=0 && co(1+11,7)<=0 
            s2is=100;
end
if co(1,9)>=0 && co(1,10)>=0 && co(1,11)<=0 && ...
            co(1+1,9)>=0 && co(1+1,10)>=0 && co(1+1,11)<=0 && ...
            co(1+2,9)>=0 && co(1+2,10)>=0 && co(1+2,11)<=0 && ...
