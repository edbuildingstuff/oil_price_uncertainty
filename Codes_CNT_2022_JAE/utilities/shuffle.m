           co(1+11,1)>=0 && co(1+11,2)>=0 && co(1+11,3)<=0             
               s1is=100;
end
if co(1,5)>=0 && co(1,6)>=0 && co(1,7)<=0 && ...
            co(1+1,5)>=0 && co(1+1,6)>=0 && co(1+1,7)<=0 && ...
            co(1+2,5)>=0 && co(1+2,6)>=0 && co(1+2,7)<=0 && ...
            co(1+3,5)>=0 && co(1+3,6)>=0 && co(1+3,7)<=0 && ...
            co(1+4,5)>=0 && co(1+4,6)>=0 && co(1+4,7)<=0 && ...
            co(1+5,5)>=0 && co(1+5,6)>=0 && co(1+5,7)<=0 && ...
            co(1+6,5)>=0 && co(1+6,6)>=0 && co(1+6,7)<=0 && ...
