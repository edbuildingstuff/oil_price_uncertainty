% Candidate solution of structural model
irf=IRvar([B(:,1+1:end);eye(n*(p-1)) zeros(n*(p-1),n)],Atilde,n,p,H);
ir=cumsum(irf,3);   % Cumulative impulse responses (in third dimension, which is the horizon)
        
% Normalization to take sign switch into account
co=zeros(13,n*n);
   for sh=1:n
       for k=1:1+H
           co(k,1+n*(sh-1))=ir(1,sh,k)/ir(1,sh,1); 
           co(k,2+n*(sh-1))=irf(2,sh,k)/irf(1,sh,1);
           co(k,3+n*(sh-1))=irf(3,sh,k)/irf(1,sh,1);
           co(k,4+n*(sh-1))=ir(4,sh,k)/ir(1,sh,1);
       end
   end
   
% Indicator variables for whether sign restrictions hold in
% candidate solution for impulse responses
s1is=0; s2is=0; s3is=0; s4is=0;                    
        
% Check signs of impulse responses for each of the first three shocks
% 1. Flow supply shock 
% Dynamic sign restrictions after flow supply shock (to hold for 12 months on prod, rpoil, rea)
